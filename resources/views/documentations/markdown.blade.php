# CONNEXION AU RÉSEAU EN ETHERNET (PC)

[Retour Sommaire](../../README.md)

Dans ce fichier vous trouverez comment vous connecter au réseau internet de la MEUD à l’aide d’un câble ethernet (RJ45) et de vos identifiants IMT Lille Douai/Nord Europe.

## I - Les Branchements

- Reliez votre ordinateur à la prise ethernet de votre chambre de la MEUD (via câble ethernet et adaptateur pour ceux qui en ont besoin).

- Attention, seulement 1 prise ethernet vous donne accès à l'internet de la MEUD.

## II - S'identifier sur le portail

- __Sur votre ordinateur :__ Ouvrez votre navigateur favori (Chrome, Firefox, Opera, Safari, etc...). Une fenêtre vous redirige vers la page de connexion au réseau. Entrez vos identifiants IMT (prenom.nom@etu.imt-lille-douai.fr et votre mot de passe). Vous êtes à présent connecté au réseau.

## III - Si ça ne fonctionne pas

Se référer [à la documentation diagnotique réseau](diagnostique-reseau.md).

